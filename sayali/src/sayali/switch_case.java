package sayali;

public class switch_case {
	public static void main(String args[]) {
		int a=2;
		
		switch(a) {
		case 1:
			System.out.println("1st number");
			break;
		case 2:
			System.out.println("2nd number");
			break;
		case 3:
			System.out.println("3rd number");
			break;
		default:
			System.out.println("This is default");
			break;
		}
	}

}
